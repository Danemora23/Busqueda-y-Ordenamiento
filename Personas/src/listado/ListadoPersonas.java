package listado;

public class ListadoPersonas {
	private int DPI;
	private String nombre;
	private int edad;
	public int getDPI() {
		return DPI;
	}
	public void setDPI(int dPI) {
		DPI = dPI;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public int getEdad() {
		return edad;
	}
	public void setEdad(int edad) {
		this.edad = edad;
	}
	
	

}
